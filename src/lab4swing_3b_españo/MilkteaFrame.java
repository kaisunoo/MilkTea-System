        if(cmb_size.getSelectedItem().equals("Tall")){
            priceSize = 120;
            getTotal();
        }else if(cmb_size.getSelectedItem().equals("Grande")){
            priceSize = 180;
            getTotal();
        }else if(cmb_size.getSelectedItem().equals("Venti")){
            priceSize = 200;
            getTotal();
        }
