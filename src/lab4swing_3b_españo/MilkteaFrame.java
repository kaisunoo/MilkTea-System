    private void btn_changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_changeActionPerformed
        double changeTotal;
        
        if(chk_discount.isSelected()){
            discountAmount = subtotalPrice*0.2;
            lbl_discount.setText(Double.toString(discountAmount));
            totalPrice = subtotalPrice - discountAmount;
            lbl_total.setText(Double.toString(totalPrice));
            changeTotal = Double.parseDouble(txt_cash.getText()) - Double.parseDouble(lbl_total.getText());
            lbl_change.setText(Double.toString(changeTotal));
        }else {
            changeTotal = Double.parseDouble(txt_cash.getText()) - Double.parseDouble(lbl_total.getText());
            lbl_change.setText(Double.toString(changeTotal)); 
        }
    }//GEN-LAST:event_btn_changeActionPerformed

    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        String previousTotalText = lbl_totalsales.getText();
        
        if (!previousTotalText.isEmpty()) {
            double previousTotal = Double.parseDouble(previousTotalText);
            totalPrice += previousTotal;
        }
        
        int saveAnswer;
        
        saveAnswer = JOptionPane.showConfirmDialog(null, "Save Transaction?"); //import>2 parameter
        
        if(saveAnswer==JOptionPane.YES_OPTION){
            lbl_totalsales.setText(Double.toString(totalPrice));
            JOptionPane.showMessageDialog(this, "Transaction saved!");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

